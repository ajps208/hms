if ($result) {
    // Calculate the attendance percentage for each month
    $months = array();
    while ($record = mysqli_fetch_assoc($result)) {
        $date = new DateTime($record['date']);
        $month = $date->format('F');
        if (!isset($months[$month])) {
            $months[$month] = array('present' => 0, 'total' => 0);
        }
        if ($record['attendence'] == 'present') {
            $months[$month]['present']++;
        }
        $months[$month]['total']++;
    }
    $percentages = array();
    foreach ($months as $month => $data) {
        $percent = ($data['present'] / $data['total']) * 100;
        $percentages[$month] = round($percent, 2);
